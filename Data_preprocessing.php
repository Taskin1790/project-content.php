       <h1> Data Preprocessing </h1>
<p>Data preprocessing is a process of preparing the raw data and making it suitable for a machine learning model. It is the first and crucial step while creating a machine learning model.

When creating a machine learning project, it is not always a case that we come across the clean and formatted data. And while doing any operation with data, it is mandatory to clean it and put in a formatted way. So for this, we use data preprocessing task.</p>       
<h2>Why do we need Data Preprocessing?</h2>
<p>A real-world data generally contains noises, missing values, and maybe in an unusable format which cannot be directly used for machine learning models. Data preprocessing is required tasks for cleaning the data and making it suitable for a machine learning model which also increases the accuracy and efficiency of a machine learning model.</p>
<p>It involves below steps:</p>

<ul> 
    <li>Getting the dataset.</li> 
    <li>Importing libraries.</li>
    <li>Importing datasets.</li>
    <li>Finding Missing Data.</li>
    <li>Encoding Categorical Data</li>
    <li>Splitting dataset into training and test set</li>
    <li>Feature scaling</li>
</ul>
<h2> Get the Dataset</h2>

<p>To create a machine learning model, the first thing we required is a dataset as a machine learning model completely works on data. The collected data for a particular problem in a proper format is known as the dataset.

Dataset may be of different formats for different purposes, such as, if we want to create a machine learning model for business purpose, then dataset will be different with the dataset required for a liver patient. So each dataset is different from another dataset. To use the dataset in our code, we usually put it into a CSV file. However, sometimes, we may also need to use an HTML or xlsx file</p>
</ul>

