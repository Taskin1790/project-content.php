       <h1> Regression vs. Classification in Machine Learning</h1> 
       <p>Regression and Classification algorithms are Supervised Learning algorithms. Both the algorithms are used for prediction in Machine learning and work with the labeled datasets. But the difference between both is how they are used for different machine learning problems.

The main difference between Regression and Classification algorithms that Regression algorithms are used to predict the continuous values such as price, salary, age, etc. and Classification algorithms are used to predict/Classify the discrete values such as Male or Female, True or False, Spam or Not Spam, etc</p>      

<h2>Classification</h2>
<p>Classification is a process of finding a function which helps in dividing the dataset into classes based on different parameters. In Classification, a computer program is trained on the training dataset and based on that training, it categorizes the data into different classes.</p>
<h2>Types of ML Classification Algorithms:</h2>

<ul> 
<li>Logistic Regression</li>
<li>K-Nearest Neighbours</li>
<li>Support Vector Machines</li>
<li>Kernel SVM</li>
<li>Naïve Bayes</li>
<li>Decision Tree Classification</li>
<li>Random Forest Classification</li>
</ul>
  <h2>Regression:</h2>
   <p>Regression is a process of finding the correlations between dependent and independent variables. It helps in predicting the continuous variables such as prediction of Market Trends, prediction of House prices, etc.

The task of the Regression algorithm is to find the mapping function to map the input variable(x) to the continuous output variable(y).</p>
<h2>Types of Regression Algorithm:</h2>

   <ul> 
   <li>Simple Linear Regression</li>
   <li>Multiple Linear Regression</li>
   <li>Polynomial Regression</li>
   <li>Support Vector Regression</li>
   <li>Decision Tree Regression</li>
   <li>Random Forest Regression</li>
</ul>

