       <h1> Learning Path </h1>
<p>This learning path is designed for anyone interested in quickly getting up to speed with machine learning. This learning path consists of step-by-step tutorials with hands-on demonstrations where you will build models and use them in apps.</p>       
<h2>Understand underlying models</h2>
<p>once you have mastered the machine learning project, you should learn the working of the models such as Linear Regression, Logistic Regression, Decision Trees, Support vector machines and ensemble learning

In linear regression, you will learn how to draw a straight line through the data points such that the line can be used to predict the outcome. Please note that when there is only one characteristic or feature in data, we can draw the line. If there are many features in the example dataset, we draw a multidimensional plane called a hyperplane.

The logistic regression is basically classification (covered in Classification Topic at CloudxLab) i.e. separating one object from another. In logistic regression, we basically learn to separate the instances using a straight line or plane. You should complement your learning with more projects such as (Noise removal from images, Predicting Titanic Passenger Survival, Building Spam Classifier).

You will also learn how to train the models. This is covered in Training Models at CloudxLab..</p>
<h2> Unsupervised Learning and Dimensionality Reduction</h2>
<p>The area of machine learning which deals with problems where the labels are not provided is called Unsupervised Learning. This is fairly complex and is a continuously improving are.

In dimensionality reduction, we learn about how can we reduce the dimensions i.e the features of our data. This helps in reducing the complexity and also overfitting. This is covered in Dimensionality Reduction topic.</p>


