       <h1> Logistic Regression in Machine Learning</h1>


<ul> 
    <li>Logistic regression is one of the most popular Machine Learning algorithms, which comes under the Supervised Learning technique. It is used for predicting the categorical dependent variable using a given set of independent variables.</li>
    <li>Logistic Regression is much similar to the Linear Regression except that how they are used. Linear Regression is used for solving Regression problems, whereas Logistic regression is used for solving the classification problems.</li>
    <li>In Logistic regression, instead of fitting a regression line, we fit an "S" shaped logistic function, which predicts two maximum values (0 or 1).</li>
    <li>The curve from the logistic function indicates the likelihood of something such as whether the cells are cancerous or not, a mouse is obese or not based on its weight, etc</li>
    <li>Logistic Regression is a significant machine learning algorithm because it has the ability to provide probabilities and classify new data using continuous and discrete datasets.</li>

</ul>
  
  <h2>Logistic Function (Sigmoid Function)</h2>
   <ul>
     <li>The sigmoid function is a mathematical function used to map the predicted values to probabilities.</li>
     <li>It maps any real value into another value within a range of 0 and 1.</li>
     <li>The value of the logistic regression must be between 0 and 1, which cannot go beyond this limit, so it forms a curve like the "S" form. The S-form curve is called the Sigmoid function or the logistic function.</li>



   <h2>Type of Logistic Regression</h2>
   <p>On the basis of the categories, Logistic Regression can be classified into three types:</p>
   <ul> 
    <li>Binomial: In binomial Logistic regression, there can be only two possible types of the dependent variables, such as 0 or 1, Pass or Fail, etc.</li> 
    <li>Multinomial: In multinomial Logistic regression, there can be 3 or more possible unordered types of the dependent variable, such as "cat", "dogs", or "sheep"</li>
    <li>Ordinal: In ordinal Logistic regression, there can be 3 or more possible ordered types of dependent variables, such as "low", "Medium", or "High"..</li>
    
</ul>

